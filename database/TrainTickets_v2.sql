-- 1. Tạo CSDL
CREATE DATABASE IF NOT EXISTS trainticketsdb_v2;
USE trainticketsdb_v2;

-- 2. Bảng người dùng
CREATE TABLE IF NOT EXISTS user (
    id            BIGINT AUTO_INCREMENT PRIMARY KEY,
    username      VARCHAR(25)  NOT NULL UNIQUE,
    password      VARCHAR(100) NOT NULL,
    fullname      VARCHAR(50)  NOT NULL,
    email         VARCHAR(50)  NOT NULL UNIQUE,
    phoneNumber   VARCHAR(15)  NOT NULL UNIQUE,
    gender        BIT          NOT NULL,
    address       VARCHAR(200) NOT NULL,
    birthday      DATE,
    role          VARCHAR(20)  NOT NULL,
    status        INT DEFAULT 1,
    createdAt     DATETIME NOT NULL,
    updatedAt     DATETIME
);

-- 3. Nhà ga
CREATE TABLE IF NOT EXISTS station (
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    name      VARCHAR(100) NOT NULL UNIQUE,
    location  VARCHAR(200) NOT NULL,
    createdAt DATETIME NOT NULL,
    updatedAt DATETIME
);

-- 4. Tàu
CREATE TABLE IF NOT EXISTS train (
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    name      VARCHAR(50)  NOT NULL,
    code      VARCHAR(20)  NOT NULL UNIQUE,
    capacity  SMALLINT     NOT NULL,
    createdAt DATETIME     NOT NULL,
    updatedAt DATETIME
);

-- 5. Toa tàu
CREATE TABLE IF NOT EXISTS coach (
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    trainId   BIGINT NOT NULL,
    code      VARCHAR(10) NOT NULL,
    type      VARCHAR(20) NOT NULL,
    position  INT,
    capacity  SMALLINT NOT NULL,
    FOREIGN KEY (trainId) REFERENCES train(id),
    UNIQUE (trainId, code)
);

-- Xóa FK cũ của coach
ALTER TABLE coach DROP FOREIGN KEY coach_ibfk_1;

-- Thêm FK mới với ON DELETE CASCADE
ALTER TABLE coach
ADD CONSTRAINT coach_ibfk_1 FOREIGN KEY (trainId) REFERENCES train(id) ON DELETE CASCADE;

-- 6. Ghế
CREATE TABLE IF NOT EXISTS seat (
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    coachId   BIGINT NOT NULL,
    seatCode  VARCHAR(10) NOT NULL,
    type      VARCHAR(20) DEFAULT 'standard',
    FOREIGN KEY (coachId) REFERENCES coach(id),
    UNIQUE (coachId, seatCode)
);

-- Xóa FK cũ của seat
ALTER TABLE seat DROP FOREIGN KEY seat_ibfk_1;

-- Thêm FK mới với ON DELETE CASCADE
ALTER TABLE seat
ADD CONSTRAINT seat_ibfk_1 FOREIGN KEY (coachId) REFERENCES coach(id) ON DELETE CASCADE;

-- 7. Tuyến đường
CREATE TABLE IF NOT EXISTS route (
    id          BIGINT AUTO_INCREMENT PRIMARY KEY,
    departureId BIGINT NOT NULL,
    arrivalId   BIGINT NOT NULL,
    duration    TIME NOT NULL,
    distanceKm  FLOAT NOT NULL,
    createdAt   DATETIME NOT NULL,
    updatedAt   DATETIME,
    FOREIGN KEY (departureId) REFERENCES station(id),
    FOREIGN KEY (arrivalId) REFERENCES station(id),
    UNIQUE (departureId, arrivalId)
);

-- 8. Chuyến tàu
CREATE TABLE IF NOT EXISTS trip (
    id          BIGINT AUTO_INCREMENT PRIMARY KEY,
    trainId     BIGINT NOT NULL,
    routeId     BIGINT NOT NULL,
    departureAt DATETIME NOT NULL,
    arrivalAt   DATETIME NOT NULL,
    price       FLOAT NOT NULL,
    status      TINYINT NOT NULL,
    createdAt   DATETIME NOT NULL,
    updatedAt   DATETIME,
    FOREIGN KEY (trainId) REFERENCES train(id),
    FOREIGN KEY (routeId) REFERENCES route(id)
);

-- 9. Vé
CREATE TABLE IF NOT EXISTS ticket (
    id         BIGINT AUTO_INCREMENT PRIMARY KEY,
    userId     BIGINT NOT NULL,
    tripId     BIGINT NOT NULL,
    seatId     BIGINT NOT NULL,
    price      FLOAT NOT NULL,
    status     TINYINT NOT NULL, -- 0: chưa thanh toán, 1: đã thanh toán, 2: huỷ
    ticketType TINYINT NOT NULL DEFAULT 0, -- 0: 1 chiều, 1: chiều đi, 2: chiều về
    createdAt  DATETIME NOT NULL,
    updatedAt  DATETIME,
    FOREIGN KEY (userId) REFERENCES user(id),
    FOREIGN KEY (tripId) REFERENCES trip(id),
    FOREIGN KEY (seatId) REFERENCES seat(id),
    UNIQUE (tripId, seatId)
);

-- 10. Mã giảm giá
CREATE TABLE IF NOT EXISTS discount (
    id                BIGINT AUTO_INCREMENT PRIMARY KEY,
    code              VARCHAR(30) NOT NULL UNIQUE,
    description       VARCHAR(100),
    discountType      VARCHAR(20) NOT NULL, -- 'PERCENT', 'AMOUNT'
    value             FLOAT NOT NULL,
    maxDiscount       FLOAT DEFAULT NULL,
    minOrderAmount    FLOAT DEFAULT NULL,
    quantityAvailable INT DEFAULT NULL,
    isActive          BOOLEAN DEFAULT TRUE,
    expiredAt         DATETIME NOT NULL,
    createdAt         DATETIME NOT NULL
);

-- 11. Đơn hàng
CREATE TABLE IF NOT EXISTS orders (
    id           BIGINT AUTO_INCREMENT PRIMARY KEY,
    userId       BIGINT NOT NULL,
    discountId   BIGINT,
    totalAmount  FLOAT NOT NULL,
    finalAmount  FLOAT NOT NULL,
    returnTripId BIGINT, -- nếu là khứ hồi
    status       TINYINT NOT NULL, -- 0: chờ, 1: thành công, 2: thất bại
    createdAt    DATETIME NOT NULL,
    updatedAt    DATETIME,
    FOREIGN KEY (userId) REFERENCES user(id),
    FOREIGN KEY (discountId) REFERENCES discount(id),
    FOREIGN KEY (returnTripId) REFERENCES trip(id)
);

-- 12. Chi tiết đơn hàng
CREATE TABLE IF NOT EXISTS order_ticket (
    id        BIGINT AUTO_INCREMENT PRIMARY KEY,
    orderId   BIGINT NOT NULL,
    ticketId  BIGINT NOT NULL,
    FOREIGN KEY (orderId) REFERENCES orders(id),
    FOREIGN KEY (ticketId) REFERENCES ticket(id),
    UNIQUE (orderId, ticketId)
);

-- 13. Thanh toán
CREATE TABLE IF NOT EXISTS payment (
    id            BIGINT AUTO_INCREMENT PRIMARY KEY,
    orderId       BIGINT NOT NULL,
    method        VARCHAR(30) NOT NULL,
    status        ENUM('PENDING', 'SUCCESS', 'FAILED') NOT NULL,
    amount        FLOAT NOT NULL,
    paidAt        DATETIME,
    transactionId VARCHAR(100),
    FOREIGN KEY (orderId) REFERENCES orders(id),
    UNIQUE (orderId)
);


